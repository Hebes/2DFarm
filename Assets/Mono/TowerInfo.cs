public class TowerInfo
{
    /// <summary>
    /// 唯一ID
    /// </summary>
    public int id;

    /// <summary>
    /// 塔的名字
    /// </summary>
    public string name;

    /// <summary>
    /// 塔的价值
    /// </summary>
    public int money;

    /// <summary>
    /// 攻击力
    /// </summary>
    public int atk;

    /// <summary>
    /// 攻击范围
    /// </summary>
    public int atkRange;

    /// <summary>
    /// 攻击间隔时间
    /// </summary>
    public float offsetTime;

    /// <summary>
    /// 下一等级的塔ID
    /// </summary>
    public int nextLev;

    /// <summary>
    /// 塔图片路径
    /// </summary>
    public string imgRes;

    /// <summary>
    /// 塔预设体路径
    /// </summary>
    public string res;

    /// <summary>
    /// 攻击类型
    /// </summary>
    public int atkType;

    /// <summary>
    /// 特效路径
    /// </summary>
    public string eff;

}
